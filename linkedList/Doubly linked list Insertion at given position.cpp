//Function to insert a new node at given position in doubly linked list.
void addNode(Node *head, int pos, int data)
{
    //0 <= p < N
    Node *temp=new Node(data);
    if(!head)
        return;
    
    while(pos>0){
        head=head->next;
        pos--;
    }
        temp->next=head->next;
        if(head->next)
            head->next->prev=head;
        head->next=temp;
        temp->prev=head;
}