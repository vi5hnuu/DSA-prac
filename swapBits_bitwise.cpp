#include<iostream>
using namespace std;

unsigned int swapBits(unsigned int n)
    {
    	//10101010101010101010101010101010 =>2863311530
    	//01010101010101010101010101010101 =>1431655765
    	//lsb at index 1
    	int even=n & 2863311530;
    	even>>=1;
    	//23=>00000000 00000000 00000000 00010111 & 10101010101010101010101010101010
    	//00000000 00000000 00000000 00000010>>1
    	//000000000 00000000 00000000 0000001
    	int odd=n & (1431655765);
    	odd<<=1;
    	//23=>00000000 00000000 00000000 00010111 & 01010101010101010101010101010101
    	//00000000 00000000 00000000 00010101<<1
    	//0000000 00000000 00000000 000101010
    	int swapped=even | odd;
    	//00000000 00000000 00000000 00101011
    	return swapped;
    }

